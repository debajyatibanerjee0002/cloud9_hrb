package user.search.searchClass;

public class ResortSearchResult {
	String resort_id;
	String resort_name;
	String addr;
	int resort_type;
	int total_rooms;
	double resort_price;
	String available;
	public String getResort_id() {
		return resort_id;
	}
	public void setResort_id(String resort_id) {
		this.resort_id = resort_id;
	}
	public String getResort_name() {
		return resort_name;
	}
	public void setResort_name(String resort_name) {
		this.resort_name = resort_name;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public int getResort_type() {
		return resort_type;
	}
	public void setResort_type(int resort_type) {
		this.resort_type = resort_type;
	}
	public int getTotal_rooms() {
		return total_rooms;
	}
	public void setTotal_rooms(int total_rooms) {
		this.total_rooms = total_rooms;
	}
	public double getResort_price() {
		return resort_price;
	}
	public void setResort_price(double resort_price) {
		this.resort_price = resort_price;
	}
	public String getAvailable() {
		return available;
	}
	public void setAvailable(String available) {
		this.available = available;
	}
	
}
